public class NoncorrespondInterfaceException extends Exception {
	private static final long serialVersionUID = 6401461582500124919L;

	public NoncorrespondInterfaceException() {
		super();
	}

	public NoncorrespondInterfaceException(String message, Throwable cause) {
		super(message, cause);
	}

	public NoncorrespondInterfaceException(String message) {
		super(message);
	}

	public NoncorrespondInterfaceException(Throwable cause) {
		super(cause);
	}

}
